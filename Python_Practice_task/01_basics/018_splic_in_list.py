# split() method:
# The split() method is used to divide a string into a list of substrings
# based on a specified delimiter (separator).

x_string = "hello,world,hi"

# Split the string using a comma as the delimiter
x_list = x_string.split(",")

# For better understanding, try using a different character (e.g., "l")
# as the delimiter and observe how the output changes.

# If no delimiter is provided, split() defaults to whitespace (spaces, tabs, etc.)

print(x_list)