# format() method:
# The format() method is used to insert values into a string using placeholders {}.
# It allows dynamic formatting of strings by replacing placeholders with variables.

name = "Ali"
age = 20

# Insert variables into the string using format()
print("My friend's name is {} and his age is {}.".format(name, age))

